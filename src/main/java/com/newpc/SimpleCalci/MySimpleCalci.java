package com.newpc.SimpleCalci;

public class MySimpleCalci {

	int addition(int a, int b) {
		return a + b;
	}
	
	int subtraction(int a, int b) {
		return a - b;
	}
	
	int multiplication(int a, int b) {
		return a * b;
	}
	
	float division(int a, int b) {
		return (float) (a) / b;
	}
	
	int remainder(int a, int b) {
		return a % b;
	}
	
}
